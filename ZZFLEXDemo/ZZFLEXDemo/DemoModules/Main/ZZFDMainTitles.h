#ifndef ZZFDMainTitles_h
#define ZZFDMainTitles_h

#define     TITLE_ZZFLEX_VC                 @"\
ZZFlexibleLayoutViewController 是一个基于UICollectionView实现的数据驱动的列表页框架，可大大降低复杂列表界面实现和维护的难度。\n\
ZZFLEX的核心思路是UI的模块化，使用ZZFLEX我们无需再关心collectionView的各种代理方法，只需潜心实现我们需要的列表元素。\n\
它使得一个复杂界面的构建就如同拼图一般，我们只需一件件的add需要的模块，即可构建出我们想要的界面。\
"

#define     TITLE_ZZFLEX_EDIT_VC            @"\
ZZFlexibleLayoutViewController+EditExtension，是使ZZFLEXVC具有处理编辑页面能力的一种解决方案.\n\
其主要原理是将数据模型的属性与UI控件对应属性进行关联映射。\
"

#define     TITLE_ZZFLEX_RQ                 @"\
某些复杂页面的数据可能是来自多个网络请求的，然而同时发起的网络请求，请求结果返回顺序是不确定的。\n\
ZZFLEXRequestQueue设计用来保证这种业务场景下，按既定顺序依次加载展示UI。\
"

#define     TITLE_ZZFLEX_VE                 @"\
UIView+ZZFLEX 是使用链式API实现的快速编写UI代码的拓展，旨在实现View级别的模块化。\
"

#endif /* ZZFDMainTitles_h */
